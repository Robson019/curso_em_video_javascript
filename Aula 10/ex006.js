function clicar() {
  var a = document.getElementById('area')
  a.innerText = 'Clicou!'
  a.style.background = 'red'
}

function entrar() {
  var b = document.getElementById('area')
  b.innerText = 'Entrou!'
}

function sair() {
  var c = document.getElementById('area')
  c.innerText = 'Saiu!'
  c.style.background = '#c25858'
}
